function showHideNewUsernameDiv() {
    let usernameDiv = document.getElementById('new-username-div')
    if (usernameDiv.style.display === "block") {
        usernameDiv.style.display = "none"
    } else {
        usernameDiv.style.display = "block"
    }
}


function showHideNewEmailDiv() {
    let emailDiv = document.getElementById('new-email-div')
    if (emailDiv.style.display === "block") {
        emailDiv.style.display = "none"
    } else {
        emailDiv.style.display = "block"
    }
}